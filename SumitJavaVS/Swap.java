public class Swap {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a);
        System.out.println(b);
       
        int  temp = a;
        a = b;
        b = temp;
        System.out.println(+a);
        System.out.println(+b);
    }
    
}
